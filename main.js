function createGame(player1, hour, player2) {
    return `
    <li>
    <img src='./assets/countries/icon-${player1}.svg' alt="Bandeira do ${player1}">
        <strong>${hour}</strong>
        <img src='./assets/countries/icon-${player2}.svg' alt="Bandeira do ${player2}">
        </li>`
}
    
let delayCardAnimation = -0.3;

function createCard(date, day, games) {
    delayCardAnimation = delayCardAnimation + 0.3;
    return `
    <div class="card" style=animation-delay:${delayCardAnimation}s>
    <h2>${date} <span>${day}</span></h2>
    <ul>
    ${games}                                  
    </ul>
    </div>`
}    

function createCalendar(calendar) {
    let resultCard = "";
    let resultGames = "";
    for (const data in calendar) {
        const games = calendar[data]["games"]
        for (const game in games) {
            resultGames += createGame(
                                calendar[data]["games"][game]["time1"],
                                calendar[data]["games"][game]["hour"],
                                calendar[data]["games"][game]["time2"]
                                );
        }
        resultCard += createCard(data, calendar[data]["week"], resultGames);
        resultGames = ""      
    }

    return resultCard
}

document.querySelector('#cards').
        innerHTML = createCalendar(jsonCalendar);
